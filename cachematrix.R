## makeCacheMatrix: Generates a list of functions to perform.
##  input is a matrix to be inversed 
makeCacheMatrix <- function(x = matrix()) {
  s <- NULL
  set <- function(y) {
    x <<- y
    s <<- NULL
  }
  get <- function() x
  setinverse <- function(solve) s <<- solve
  getinverse <- function() s
  list(set = set, get = get,
       setinverse = setinverse,
       getinverse = getinverse)
}

## cacheSolve: Takes in the list of functions generated
##  by makeCacheMatrix, and either returns the cached matrix inverse
##  or calculated matrix inverse
cacheSolve <- function(x, ...) {

  ## Return a matrix that is the inverse of 'x'
  s <- x$getinverse()
  if(!is.null(s)) {
    message("getting cached data")
    return(s)
  }
  data <- x$get()
  s <- solve(data, ...)
  x$setinverse(s)
  s
}
